-- Retorna um JSON com TODOS os dados do usuário (perfil + progresso + notas + comentários)
create or replace function public.export_my_data()
returns jsonb
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  uid uuid := auth.uid();
  result jsonb;
begin
  if uid is null then
    raise exception 'Usuário não autenticado';
  end if;

  select jsonb_build_object(
    'exportado_em', now(),
    'perfil',      (select to_jsonb(p.*) from public.perfis p where p.id = uid),
    'auth',        (select jsonb_build_object('email', u.email, 'criado_em', u.created_at, 'ultimo_login', u.last_sign_in_at) from auth.users u where u.id = uid),
    'progresso',   coalesce((select jsonb_agg(to_jsonb(x.*)) from public.progresso_aulas x where x.user_id = uid), '[]'::jsonb),
    'notas',       coalesce((select jsonb_agg(to_jsonb(x.*)) from public.notas_aulas       x where x.user_id = uid), '[]'::jsonb),
    'comentarios', coalesce((select jsonb_agg(to_jsonb(x.*)) from public.comentarios_aulas x where x.user_id = uid), '[]'::jsonb)
  ) into result;

  return result;
end;
$$;

revoke all on function public.export_my_data() from public, anon;
grant execute on function public.export_my_data() to authenticated;

-- Deleta a conta do usuário autenticado (e dados em cascata via FK)
create or replace function public.delete_my_account()
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  uid uuid := auth.uid();
begin
  if uid is null then
    raise exception 'Usuário não autenticado';
  end if;

  -- delete em auth.users → cascata via FK em perfis, progresso, notas, comentarios
  delete from auth.users where id = uid;
end;
$$;

revoke all on function public.delete_my_account() from public, anon;
grant execute on function public.delete_my_account() to authenticated;
